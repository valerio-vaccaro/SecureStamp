for code in LANGUAGES:
    if code == "en":
        continue
    TRANSLATIONS[code] = {**TRANSLATIONS["en"], **TRANSLATIONS.get(code, {})}


def get_language():
    selected = session.get("language", DEFAULT_LANGUAGE)
    return selected if selected in LANGUAGES else DEFAULT_LANGUAGE


def set_language(language_code):
    session["language"] = language_code if language_code in LANGUAGES else DEFAULT_LANGUAGE


def normalize_language(language_code, default=DEFAULT_LANGUAGE):
    return language_code if language_code in LANGUAGES else default


def translate(key, **kwargs):
    language = getattr(g, "language", DEFAULT_LANGUAGE)
    template = TRANSLATIONS.get(language, {}).get(key) or TRANSLATIONS["en"].get(key) or key
    return template.format(**kwargs) if kwargs else template


def translate_status(status):
    status_map = {
        "Timestamp requested": "status.timestamp_requested",
        "Timestamp completed": "status.timestamp_completed",
        "Error": "status.error",
    }
    return translate(status_map.get(status, status))


def init_i18n(app):
    @app.before_request
    def load_language():
        g.language = get_language()

    @app.context_processor
    def inject_i18n_helpers():
        endpoint = request.endpoint or "main.dashboard"
        return {
            "t": translate,
            "translate_status": translate_status,
            "available_languages": LANGUAGES,
            "current_language": g.language,
            "default_language": DEFAULT_LANGUAGE,
            "current_endpoint": endpoint,
        }
